/**
 * Simple color representation using four float values.
 */
public struct Color
{
    public float r;		///< Red component
    public float g;		///< Green component
    public float b;		///< Blue component
    public float a;		///< Alpha component
};

public struct Color32
{
    public byte r;
    public byte g;
    public byte b;
    public byte a;
}